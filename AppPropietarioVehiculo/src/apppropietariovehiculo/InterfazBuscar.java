    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(txtBuscar.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo vacio", "Advertencia", 2);
            txtBuscar.setText("");
            txtBuscar.requestFocus();
        }else{
            buscar = txtBuscar.getText(); 
            rpt = false;
            for (int i = 0; i < Main.persona.size(); i++){
                p = Main.persona.get(i);
                v = Main.vehiculo.get(i);
                if(buscar.equalsIgnoreCase(p.getDocumento())){
                    rpt = true;
                    
                    int valor = (v.getValor());
                    float tarifa = (v.getTarifa());
                    
                    lbPropietario.setText(toHtmlText(lbPropietario.getText() + "Nombres: " + p.getPrimerNombre() + " " + p.getSegundoNombre() + " " + p.getApellidos() + "\n"));
                    lbVehiculo.setText(toHtmlText(lbVehiculo.getText() + "Placa: "  + v.getPlaca() + "\n" + "Impuesto: " + valor * tarifa));
                    JOptionPane.showMessageDialog(null, "Numero de documento encontrado", "OK", 1);
                    txtBuscar.setText("");
                break;
                }      
            }
            if (rpt == false){
                JOptionPane.showMessageDialog(null, "Numero de documento NO encontrado", "Advertencia", 2);
            }
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

